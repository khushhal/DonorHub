                                <div class="section-2 col-lg-12 col-md-12 col-sm-12 col-xs-12">
                                   <p>Donating blood is one of easiest ways to support your community. Your donation will help save the life of up to three people! If you are never donated blood before, you are probably wondering what you can expect and you may even be a little nervous, but don't worry! Giving blood is safe and simple. The entire process takes about one hour and 15 minutes; the actual donation of a pint of whole blood unit takes 8 to 10 minutes. However, the time varies slightly with each person depending on several factors including the donor's health history and attendance at the blood drive. </p>
                                   <p>
                                   <b>Step 1 Registration</b><br />
Present your photo ID Card.
Read information sheet about donating blood.<br />
<b>Step 2 Donor Health History</b><br />
Provide basic demographic and health information
Answer questions about past and present health history (information is kept private)
Undergo mini exam to determine if you are able to donate (temperature, blood pressure, heart rate). Give blood drop to determine hemoglobin level<br />
<b>Step 3 Hydration Station</b><br />
Drink water or other beverage prior to your donation. The more hydrated you are, the faster your donation will be and the better you will feel afterwards<br />
<b>Step 4 Donation</b><br />
Sit in one of our specialized comfy donor
Answer a few questions from a staff member
Once the needle is in place, it usually takes less than 10 minutes to draw a unit (about a pint) of blood. You will squeeze a ball to keep the blood flowing while you read, relax or chat with one of our nurses
Finish your donation, after which the needle will be removed and a bandage applied (the bandage is a great way to show off what a hero you are!)<br />
<b>Step 5 Visit the Canteen</b><br />
Sit, relax and have a snack and drink at our donor canteen for a few minutes (California law states you will need to hang out with us for at least 15 minutes)
All Done! Enjoy your day. You have earned it! You have found the Hero in you!<br />
<b>Step 6 After Donation</b><br />
Avoid strenuous physical activity or heavy lifting for a few hours. If you feel light-headed, lie down until feeling better
